    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        try {
        peminjamCrudService = new ControllerPeminjam(new ConnectionManager());

        String idPeminjamanStr = txt_search.getText().trim();
        if (idPeminjamanStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID Peminjaman harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int id_peminjaman;
        try {
            id_peminjaman = Integer.parseInt(idPeminjamanStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID Peminjaman harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (txt_nama.getText().trim().isEmpty()
                || txt_notelp.getText().trim().isEmpty()
                || txt_alamat.getText().trim().isEmpty()
                || txt_tgl_pengambilan.getText().trim().isEmpty()
                || txt_durasi_sewa.getText().trim().isEmpty()
                || txt_nama_ps.getText().trim().isEmpty()
                || txt_harga.getText().trim().isEmpty()
                || txt_total_harga.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String nama = txt_nama.getText().trim();
        String no_telp = txt_notelp.getText().trim();
        String alamat = txt_alamat.getText().trim();
        String ps = txt_nama_ps.getText().trim();
        double harga = Double.parseDouble(txt_harga.getText().trim());
        double total_biaya = Double.parseDouble(txt_total_harga.getText().trim());
        int durasi = Integer.parseInt(txt_durasi_sewa.getText().trim());

        LocalDate tglPengambilan = LocalDate.parse(txt_tgl_pengambilan.getText().trim());
        LocalDate tglTenggat = tglPengambilan.plusDays(durasi);

        Peminjam peminjam = new Peminjam();
        peminjam.setNama_Penyewa(nama);
        peminjam.setNo_Telp(no_telp);
        peminjam.setAlamat(alamat);
        peminjam.setPs(ps);
        peminjam.setDurasi_Hari(durasi);
        peminjam.setTgl_Pengambilan(tglPengambilan.toString());
        peminjam.setTgl_Tenggat(tglTenggat.toString());
        peminjam.setHarga(harga);
        peminjam.setTotal_Biaya(total_biaya);

        peminjamCrudService.update(id_peminjaman, peminjam);

        JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!", "Success", JOptionPane.INFORMATION_MESSAGE);
        loadData(); // Memuat ulang data ke tabel
        emptyField(); // Mengosongkan field input
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Format angka tidak valid! Periksa kembali input Durasi atau Harga.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btn_updateActionPerformed

