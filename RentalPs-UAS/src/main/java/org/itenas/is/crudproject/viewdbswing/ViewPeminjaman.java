    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        // TODO add your handling code here:
        String searchId = txt_search.getText().trim();

    if (searchId.isEmpty()) {
        JOptionPane.showMessageDialog(this, "ID Peminjaman tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        int idPeminjaman = Integer.parseInt(searchId);

        peminjamCrudService = new ControllerPeminjam(new ConnectionManager());
        Peminjam peminjam = peminjamCrudService.findOne(idPeminjaman); // Cari data berdasarkan ID

        DefaultTableModel tableModel = (DefaultTableModel) table_Peminjaman.getModel();
        tableModel.setRowCount(0); 

        if (peminjam != null) {
            Object[] rowData = {
                peminjam.getId_Peminjaman(),
                peminjam.getNama_Penyewa(),
                peminjam.getNo_Telp(),
                peminjam.getAlamat(),
                peminjam.getPs(),
                peminjam.getDurasi_Hari(),
                peminjam.getTgl_Pengambilan(),
                peminjam.getTgl_Tenggat(),
                peminjam.getHarga(),
                peminjam.getTotal_Biaya()
            };
            tableModel.addRow(rowData); 
        } else {
            JOptionPane.showMessageDialog(this, "Data dengan ID Peminjaman tidak ditemukan!", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID Peminjaman harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btn_searchActionPerformed

