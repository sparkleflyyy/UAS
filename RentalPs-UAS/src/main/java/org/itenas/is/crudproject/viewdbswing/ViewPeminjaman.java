        peminjam.setNo_Telp(no_telp);
        peminjam.setAlamat(alamat);
        peminjam.setPs(ps);
        peminjam.setDurasi_Hari(durasi);
        peminjam.setTgl_Pengambilan(tglPengambilan.toString());
        peminjam.setTgl_Tenggat(tglTenggat.toString());
