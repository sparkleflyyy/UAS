    private void table_pengembalianMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_pengembalianMouseClicked
            try {
         // Deklarasi variabel
         String nama_penyewa, alamat, no_telp, tgl_pengambilan, tgl_tenggat, nama_ps, tgl_pengembalian;
         double hargaPerHari, total_Harga, total_Denda;
         int durasi, id_peminjaman;

         // Ambil baris yang dipilih di tabel
         int row = table_pengembalian.getSelectedRow();

         // Parsing nilai dari tabel menggunakan metode bantu
         id_peminjaman = parseInteger(table_pengembalian.getValueAt(row, 0));
         nama_penyewa = parseString(table_pengembalian.getValueAt(row, 1));
         no_telp = parseString(table_pengembalian.getValueAt(row, 2));
         alamat = parseString(table_pengembalian.getValueAt(row, 3));
         nama_ps = parseString(table_pengembalian.getValueAt(row, 4));
         durasi = parseInteger(table_pengembalian.getValueAt(row, 5));
         tgl_pengambilan = parseString(table_pengembalian.getValueAt(row, 6));
         tgl_tenggat = parseString(table_pengembalian.getValueAt(row, 7));
         hargaPerHari = parseDouble(table_pengembalian.getValueAt(row, 8));
         total_Harga = parseDouble(table_pengembalian.getValueAt(row, 9));
         tgl_pengembalian = parseString(table_pengembalian.getValueAt(row, 10));
         total_Denda = parseDouble(table_pengembalian.getValueAt(row, 11));

         // Tampilkan nilai pada form
         txt_search.setText(String.valueOf(id_peminjaman));
         txt_search.setEnabled(false);
         txt_nama.setText(nama_penyewa);
         txt_notelp.setText(no_telp);
         txt_alamat.setText(alamat);
         txt_waktu_tenggat.setText(tgl_tenggat);
         txt_tgl_pengambilan.setText(tgl_pengambilan);
         txt_tgl_pengembalian.setText(tgl_pengembalian);
         txt_durasi_sewa.setText(String.valueOf(durasi));
         txt_nama_ps.setText(nama_ps);

         // Format harga dan total harga dengan titik sebagai pemisah desimal
         txt_harga.setText(String.format("%.2f", hargaPerHari).replace(',', '.'));
         txt_total_harga.setText(String.format("%.2f", total_Harga).replace(',', '.'));
         txt_total_denda.setText(String.format("%.2f", total_Denda).replace(',', '.'));
     } catch (Exception e) {
         // Tangani kesalahan jika ada
         JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
     }

    }//GEN-LAST:event_table_pengembalianMouseClicked

