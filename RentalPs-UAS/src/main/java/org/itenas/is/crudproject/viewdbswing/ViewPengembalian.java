    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
                // TODO add your handling code here:
         peminjamCrudService = new ControllerPeminjam(new ConnectionManager());

    // Ambil ID Peminjaman dari input
    String id = txt_search.getText().trim();

    // Validasi apakah ID kosong
    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "ID Peminjaman harus diisi sebelum menghapus data!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Konfirmasi penghapusan data
    int dialogResult = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (dialogResult == JOptionPane.YES_OPTION) {
        try {
            // Hapus data berdasarkan ID dan cek keberhasilannya
            boolean isDeleted = peminjamCrudService.delete(Integer.parseInt(id));
            if (isDeleted) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                loadData(); // Refresh tabel setelah penghapusan
                emptyField(); // Kosongkan input field
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal dihapus. ID tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID Peminjaman harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btn_deleteActionPerformed

