    private void hitungDenda() {
    try {
        // Ambil input data
        double harga = Double.parseDouble(txt_harga.getText().trim());
        int durasi = Integer.parseInt(txt_durasi_sewa.getText().trim());
        LocalDate tglPengambilan = LocalDate.parse(txt_tgl_pengambilan.getText().trim());
        LocalDate tglPengembalian = LocalDate.parse(txt_tgl_pengembalian.getText().trim());

        // Hitung tanggal tenggat
        LocalDate tglTenggat = tglPengambilan.plusDays(durasi);

        // Hitung denda per hari (harga * 1.1)
        double dendaPerHari = harga * 1.1;

        // Hitung total denda
        double totalDenda = 0;
        if (tglPengembalian.isAfter(tglTenggat)) {
            long daysLate = ChronoUnit.DAYS.between(tglTenggat, tglPengembalian);
            totalDenda = daysLate * dendaPerHari;
        }

        // Tampilkan total denda di txt_total_denda
        txt_total_denda.setText(String.valueOf(totalDenda));

    } catch (NumberFormatException | DateTimeParseException e) {
        txt_total_denda.setText("0");
    }
}

