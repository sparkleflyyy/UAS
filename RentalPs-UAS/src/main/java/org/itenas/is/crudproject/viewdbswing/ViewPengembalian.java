    private void loadData() {
        peminjamCrudService = new ControllerPeminjam(new ConnectionManager());
        List<Peminjam> listPeminjam = peminjamCrudService.findAll();
        Object[][] dataPeminjam = new Object[listPeminjam.size()][12];

        int counter = 0;

        for (Peminjam peminjam : listPeminjam) {
            dataPeminjam[counter][0] = peminjam.getId_Peminjaman(); // ID Peminjaman
            dataPeminjam[counter][1] = peminjam.getNama_Penyewa();  // Nama Penyewa
            dataPeminjam[counter][2] = peminjam.getNo_Telp();       // Nomor Telepon
            dataPeminjam[counter][3] = peminjam.getAlamat();        // Alamat
            dataPeminjam[counter][4] = peminjam.getPs();            // PS
            dataPeminjam[counter][5] = peminjam.getDurasi_Hari();   // Durasi Hari
            dataPeminjam[counter][6] = peminjam.getTgl_Pengambilan(); // Tanggal Pengambilan
            dataPeminjam[counter][7] = peminjam.getTgl_Tenggat();   // Tanggal Tenggat
            dataPeminjam[counter][8] = peminjam.getHarga();   // Total Biaya
            dataPeminjam[counter][9] = peminjam.getTotal_Biaya();   // Total Biaya
            dataPeminjam[counter][10] = peminjam.getTgl_pengembalian();   // Total Biaya
            dataPeminjam[counter][11] = peminjam.getTotal_denda();   // Total Biaya

            counter++;
        }

        table_pengembalian.setModel(new javax.swing.table.DefaultTableModel(
                dataPeminjam,
                new String[]{
                    "ID Peminjaman", "Nama Penyewa", "Nomor Telepon", "Alamat", "PS", "Durasi Hari", "Tanggal Pengambilan", "Tanggal Tenggat", "Harga", "Total Biaya", "Tanggal Pengembalian", "Total Denda" 
                }
        ));
        //table_pengembalian.repaint();
    }

    private String parseString(Object value) {
        return value == null ? "" : value.toString();
    }

    private int parseInteger(Object value) {
        try {
            return value == null ? 0 : Integer.parseInt(value.toString());
        } catch (NumberFormatException e) {
            return 0;
        }
    }

    private double parseDouble(Object value) {
        try {
            return value == null ? 0 : Double.parseDouble(value.toString());
        } catch (NumberFormatException e) {
            return 0;
        }
    }

